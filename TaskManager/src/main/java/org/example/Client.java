package org.example;

public class Client
{
    public int id;
    public String name;
    public String phone;
    public String project_name;
    public int accepted;


    public Client(int id, String name, String phone, String project_name, int accepted)
    {
        this.id = id;
        this.name = name;
        this.phone = phone;
        this.project_name = project_name;
        this.accepted = accepted;
    }

}
